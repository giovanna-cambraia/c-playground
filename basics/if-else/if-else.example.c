#include <stdio.h>

int main() {
    int age = 20;

    if (age >= 18) {
        printf("You are an adult.\n");
    } else {
        printf("You are a minor.\n");
    }

    return 0;
}

// If-else statements allow you to execute different blocks of code based on certain conditions. 
// In this example, we check if the age is greater than or equal to 18. If it is, 
// we print "You are an adult." Otherwise, we print "You are a minor."


