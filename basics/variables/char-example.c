#include <stdio.h>

int main() {
    char grade = 'A';

    printf("Grade: %c\n", grade);
    return 0; 
}

// %c is a format specifier used in printf to indicate that the corresponding argument is a character. 
// In this case, it will print the value of the variable 'grade' which is 'A'.

// Don't forget to use single quotes (' ') when assigning a character value to a char variable. And
// (" ") is used for string literals, which are arrays of characters.