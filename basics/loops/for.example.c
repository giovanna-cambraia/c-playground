#include <stdio.h>

int main() {
    for (int i = 1; i <= 5; i++) {
        printf("%d\n", i);
    }

    return 0;
}

// i = 1 → starting point
// i <= 5 → condition to continue
// i++ → increase by 1 each loop

// As it appears, a for loop is a simplified form of writing a loop when compared to a while loop. Only the structure changes.